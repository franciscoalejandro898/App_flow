function Button({ variant, size, className, children, ...props }) {
  const baseClass = "px-4 py-2 rounded";
  const variantClass = variant === "ghost" ? "bg-transparent" : "bg-blue-500 text-white";
  const sizeClass = size === "icon" ? "p-2" : "";

  return (
    <button {...props} className={`${baseClass} ${variantClass} ${sizeClass} ${className}`}>
      {children}
    </button>
  );
}

